#include "../includes/ft_printf.h"

void	ft_putstr(char *s, int *cp)
{
	int	i;

	if (!s)
	{
		ft_putstr("(null)", cp);
		return ;
	}
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar(s[i], cp);
		i++;
	}
}
